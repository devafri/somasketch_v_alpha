                    <h4 class="header-text">Somatization: Physical symptoms of mental or emotional distress</h4>
                    <p>
                        Mental illness is NOT all in your head. Many experience physical symptoms without noticing the cognitive and emotional symptoms that are used most commonly for diagnosis. Today, the average time between diagnosis and treatment is 11 years. More than a decade. We are creating tools that focus on the somatic (less stigma-laden) symptoms of mental illness in hopes of making diagnosis and communication easier and more accessible than ever.<br>
                    </p>
                    
