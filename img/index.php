                                    <h4>DRAW</h4>
                                    <p>Draw how your body feels:</p>
                                    <p>Did you know that emotions can wreak havoc in your body, even if you are not aware of them?! Through a process called somatization buried psychological or emotional distress can cause headaches, unexplained numbness, chiropractic issues, irritable bowels, and much more. Depression and anxiety are real medical illnesses that can show up as physical symptoms, especially in the strongest among us.</p>
                                      <p>Paying attention to bodily sensations not only stops thought spirals and dissociation by getting you out of your head (and into), it is the first step towards emotional awareness and wellness!</p>
